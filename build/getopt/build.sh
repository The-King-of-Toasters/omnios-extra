. ../../lib/build.sh

PROG=getopt
VER=1.1.6
PKG=ooce/util/getopt
SUMMARY="$PROG - parse command-line arguments from shell scripts"
DESC="A program to help shell scripts parse command-line parameters"

OPREFIX=$PREFIX
PREFIX+=/$PROG

set_arch 64
set_clangver

XFORM_ARGS="
    -DPREFIX=${PREFIX#/}
    -DOPREFIX=${OPREFIX#/}
    -DPROG=$PROG
    -DPKGROOT=$PROG
"

pre_configure() {
    typeset arch=$1

    subsume_arch $arch CFLAGS
    export PREFIX CFLAGS

    # no configure
    false
}

init
download_source $PROG $PROG $VER
patch_source
prep_build
build
make_package
clean_up

